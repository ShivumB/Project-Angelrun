	// keys of keyCode to false, perform actions on key release
	public void keyReleased() {
		// record when key is no longer pressed
		keys.put(keyCode, false);

		// level editor
		// initiate level editor
		if (scene.equals("title") && keyCode == 68) {
			scene = "levelEditor";
			devMode = true;
			level = levels.length - 1;
			rainMusic.close();
			gameMusic[level].setGain(0);
		}

		// print the level
		if (scene.equals("levelEditor") && keyCode == 32) {
			getLevel();

			// test the level
		} else if (scene.equals("levelEditor") && keyCode == 76) {

			int level[][] = new int[design.size()][design.get(0).size()];

			for (int i = 0; i < design.size(); i++) {
				for (int j = 0; j < design.get(0).size(); j++) {
					level[i][j] = design.get(i).get(j);
				}
			}

			load(level);
			scene = "game";
			titleMusic.pause();
			gameMusic[this.level].play();

		}

		// go back to level editor
		if (devMode && scene.equals("game") && keyCode == 75) {
			scene = "levelEditor";
			gameMusic[level].pause();
			titleMusic.play();
		}

		// OUTSIDE OF LEVEL EDITOR
		// skip level
		if (scene.equals("game") && cheats && keyCode == 61) {
			level++;
			payton.setSpeedCounter(0);
			frame = 0;
			scene = "levelAnimation";
		}

		// mute sound
		if (keyCode == 77) {
			if (mute) {

				// unmute all resources
				sfxLightning.unmute();
				sfxDeath.unmute();
				sfxCoin.unmute();

				for (int i = 0; i < gameMusic.length; i++) {
					gameMusic[i].unmute();
				}

				titleMusic.unmute();
				winMusic.unmute();
				rainMusic.unmute();

				mute = false;
			} else {

				// mute all resources
				sfxLightning.mute();
				sfxDeath.mute();
				sfxCoin.mute();

				for (int i = 0; i < gameMusic.length; i++) {
					gameMusic[i].mute();
				}

				titleMusic.mute();
				winMusic.mute();
				rainMusic.mute();

				mute = true;
			}
		}

	}

	// mouse handling
	public void mouseReleased() {
		switch (scene) {
		case "title":
			if (playButton.checkMouse()) {
				frame = 0;
				pVelY = 0;
				scene = "storyAnimation";
			}
			break;

		case "win":
			if (titleButton.checkMouse())
				scene = "title";
			break;

		case "levelEditor":
			if (mouseY >= 300) {
				if (sRight.checkMouse()) {
					scroll -= 80;
				}
				if (sLeft.checkMouse()) {
					scroll += 80;
				}
				if (expand.checkMouse()) {
					numCols++;

					for (int i = 0; i < design.size(); i++) {
						design.get(i).add(0);
					}

				}
				if (shrink.checkMouse()) {

					if (design.get(0).size() > 0) {
						numCols--;

						for (int i = 0; i < design.size(); i++) {

							int last = design.get(i).size() - 1;

							if (design.get(i).get(last) == 2) {
								addPortal = true;
							} else if (design.get(i).get(last) == 5) {
								addSpawn = true;
							}

							design.get(i).remove(last);
						}
					}
				}

				for (int i = 0; i < selects.length; i++) {
					if (selects[i].checkMouse())
						select = i;
				}

			}

			break;
		}
	}

	// level editor mouse handling
	public void drawLevelMouse() {

		// top half of screen => draw stuff
		if (mouseY < 300) {

			int xIndex = (int) ((mouseX - scroll) / 20);
			int yIndex = (int) (mouseY / 20);

			if (xIndex >= 0 && xIndex < numCols && yIndex >= 0 && yIndex < 15) {

				if (design.get(yIndex).get(xIndex) != 0 && select != 0)
					return;

				// to ensure that portals and spawns only made once
				if ((select != 2 && select != 5) || (select == 2 && addPortal) || (select == 5 && addSpawn)) {

					if (select == 0) {
						if (design.get(yIndex).get(xIndex) == 2) {
							addPortal = true;
						} else if (design.get(yIndex).get(xIndex) == 5) {
							addSpawn = true;
						}
					}

					design.get(yIndex).set(xIndex, select);
				}

				if (select == 5)
					addSpawn = false;
				if (select == 2)
					addPortal = false;

			}
		}

	}

